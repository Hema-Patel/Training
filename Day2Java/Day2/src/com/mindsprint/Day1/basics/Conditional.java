package com.mindsprint.Day1.basics;

public class Conditional {
    public static void main(String[]args)
    {
        int num1=10;
        int num2=8;
        System.out.println("num1>num2:"+(num1>num2));
        System.out.println("num1<num2:"+(num1<num2));
        System.out.println("num1>=num2:"+(num1>=num2));
        System.out.println("num1<=num2:"+(num1<=num2));
        System.out.println("num1!=num2:"+(num1!=num2));
        System.out.println("num1==num2:"+(num1==num2));

    }
}
